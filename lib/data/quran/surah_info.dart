/// معلومات ثابتة عن السور: مكية/مدنية والجزء الذي تبدأ فيه.
/// التصنيف وفق بيانات مشروع تنزيل (Tanzil).
library;

const Set<int> _medinanSurahs = {
  2, 3, 4, 5, 8, 9, 13, 22, 24, 33, 47, 48, 49, 55,
  57, 58, 59, 60, 61, 62, 63, 64, 65, 66, 76, 98, 99, 110,
};

bool isMedinan(int surahNumber) => _medinanSurahs.contains(surahNumber);

String revelationLabelAr(int surahNumber) =>
    isMedinan(surahNumber) ? 'مدنية' : 'مكية';

/// الجزء الذي تقع فيه الآية الأولى من كل سورة (1..114).
const List<int> _juzBySurah = [
  1, 1, 3, 4, 6, 7, 8, 9, 10, 11, // 1-10
  11, 12, 13, 13, 14, 14, 15, 15, 16, 16, // 11-20
  17, 17, 18, 18, 18, 19, 19, 20, 20, 21, // 21-30
  21, 21, 21, 22, 22, 22, 23, 23, 23, 24, // 31-40
  24, 25, 25, 25, 25, 26, 26, 26, 26, 26, // 41-50
  26, 27, 27, 27, 27, 27, 27, 28, 28, 28, // 51-60
  28, 28, 28, 28, 28, 28, 29, 29, 29, 29, // 61-70
  29, 29, 29, 29, 29, 29, 29, 30, 30, 30, // 71-80
  30, 30, 30, 30, 30, 30, 30, 30, 30, 30, // 81-90
  30, 30, 30, 30, 30, 30, 30, 30, 30, 30, // 91-100
  30, 30, 30, 30, 30, 30, 30, 30, 30, 30, // 101-110
  30, 30, 30, 30, // 111-114
];

int juzOfSurah(int surahNumber) => _juzBySurah[surahNumber - 1];

const _arabicDigits = ['٠', '١', '٢', '٣', '٤', '٥', '٦', '٧', '٨', '٩'];

/// يحوّل رقمًا إلى أرقام عربية مشرقية (١٢٣) لعلامات الآيات.
String arabicIndic(int number) => number
    .toString()
    .split('')
    .map((c) => _arabicDigits[int.parse(c)])
    .join();
